  // Helper methods
  private async writeTestFile(path: string, content: string): Promise<string> {
    const fullPath = join(process.cwd(), path)
    await mkdir(dirname(fullPath), { recursive: true })
    await writeFile(fullPath, content, 'utf-8')
    return path
  }

